Cypress.Commands.add('webLogin', (username, password) => {
    const user = process.env.WEB_USERNAME;
    const pw = process.env.WEB_PASSWORD;
    const webURL = process.env.APP_URL;
    cy.get(webURL);
    cy.get(username).type(user);
    cy.get(password).type(pw);
})